package org.emayor.servicehandling.utils;

/**
 * @author tku
 */
public class IOManagerException extends Exception {

    /**
     *  
     */
    public IOManagerException() {
        super();
    }

    /**
     * @param arg0
     */
    public IOManagerException(String arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     */
    public IOManagerException(Throwable arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     * @param arg1
     */
    public IOManagerException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}